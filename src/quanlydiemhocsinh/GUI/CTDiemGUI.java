    private void loadCTDiem() {
        ctDiemList = ctDiemDAO.getDSDiem(Common.idDiem);
        monHocList = monHocDAO.getMonHocs();
        DefaultTableModel tableModel = new DefaultTableModel(cols, 0);
        for (CTDiem ctd : ctDiemList) {
            Object[] obj = {ctd.getId(), ctd.getIddiem(), ctd.getIdmonhoc(), ctd.getTenmonhoc(), ctd.getSotc(), ctd.getDiem()};
            tableModel.addRow(obj);
        }
        jTable1.setModel(tableModel);

        DefaultComboBoxModel monHocModel = new DefaultComboBoxModel();
        for (MonHoc mh : monHocList) {
            monHocModel.addElement(mh.getTenmonhoc());
        }
        cb_monhoc.setSelectedIndex(0);
        cb_monhoc.setModel(monHocModel);
    }

