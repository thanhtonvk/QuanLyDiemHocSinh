    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectCB = cb_monhoc.getSelectedIndex();
        MonHoc monHoc = monHocList.get(selectCB);
        float diem = Float.parseFloat(txt_diem.getText());
        ctDiemDAO.addCTDiem(Common.idDiem, monHoc.getId(), diem);
        loadCTDiem();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        int id = (int) jTable1.getValueAt(row, 0);
        int idDiem = (int) jTable1.getValueAt(row, 1);
        int idMonHoc = (int) jTable1.getValueAt(row, 2);
        float diem = (float) jTable1.getValueAt(row, 5);
        cTDiem = new CTDiem();
        cTDiem.setId(id);
        cTDiem.setIdmonhoc(idMonHoc);
        cTDiem.setDiem(diem);
        cTDiem.setIddiem(idDiem);
        cb_monhoc.setSelectedIndex(findIndex(idMonHoc));
        txt_diem.setText(diem + "");
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (cTDiem != null) {
            int selectCB = cb_monhoc.getSelectedIndex();
            MonHoc monHoc = monHocList.get(selectCB);
            float diem = Float.parseFloat(txt_diem.getText());
            ctDiemDAO.updateCTDiem(cTDiem.getId(), cTDiem.getIddiem(), monHoc.getId(), diem);
            loadCTDiem();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (cTDiem != null) {
            ctDiemDAO.removeCTDiem(cTDiem.getId());
            loadCTDiem();
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    private int findIndex(int id) {
        int index = 0;
        for (MonHoc mh : monHocList) {
            if (mh.getId() == id) {
                index = monHocList.indexOf(mh);
            }
        }
        return index;
    }

