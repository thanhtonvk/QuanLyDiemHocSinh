    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String timkiem = txt_timkiem.getText();
        hocSinhList = hocSinhDAO.getHocSinhs();
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        for (HocSinh hs : hocSinhList) {
            if (hs.getHoten().contains(timkiem) || hs.getDiachi().contains(timkiem) || hs.getLop().contains(timkiem)) {
                Object[] obj = {hs.getId(), hs.getHoten(), hs.getNgaysinh(), hs.getDiachi(), hs.getSodienthoai(), hs.getLop()};
                tableModel.addRow(obj);
            }

        }
        jTable1.setModel(tableModel);
    }//GEN-LAST:event_jButton4ActionPerformed

