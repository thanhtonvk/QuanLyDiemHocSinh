    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        String id = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String hoten = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String ngaysinh = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String diachi = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
        String sdt = jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString();
        String lop = jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
        hocSinh = new HocSinh(Integer.parseInt(id.trim()), hoten, ngaysinh, diachi, diachi, lop);
        txt_hoten.setText(hoten);
        txt_ngaysinh.setText(ngaysinh);
        txt_diachi.setText(diachi);
        txt_lop.setText(lop);
        txt_sdt.setText(sdt);
    }//GEN-LAST:event_jTable1MouseClicked

